#include "ft_printf.h"

int	ft_put_d(int nb)
{
	int	i;

	i = 0;
	if (nb == -2147483648)
	{
		return (write(1, "-2147483648", 11));
	}
	if (nb < 0)
	{
		i += ft_put_c('-');
		nb *= -1;
	}
	if (nb > 9)
	{
		i += ft_put_d(nb / 10);
		i += ft_put_d(nb % 10);
	}
	else
	{
		i += ft_put_c(nb + 48);
	}
	return (i);
}
